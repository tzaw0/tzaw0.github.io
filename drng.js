function generate(arsenal) {
  //Primary weapon
  primary = arsenal.primary;
  let p_pick = primary[Math.floor(Math.random() * primary.length)];
  primaryName.innerHTML = p_pick.name;

  let pm_pick = "";
  p_pick.mods.forEach((m) => {
    pm_pick += Math.floor(Math.random() * m + 1);
  });
  primaryMods.innerHTML = pm_pick;

  poc_pick =
    p_pick.overclocks[Math.floor(Math.random() * p_pick.overclocks.length)];
  primaryOCFrame.src = poc_pick.type;
  primaryOCPic.src = poc_pick.pic;
  primaryOCName.innerHTML = poc_pick.name;

  //Secondary weapon
  secondary = arsenal.secondary;
  let s_pick = secondary[Math.floor(Math.random() * secondary.length)];
  secondaryName.innerHTML = s_pick.name;

  let sm_pick = "";
  s_pick.mods.forEach((m) => {
    sm_pick += Math.floor(Math.random() * m + 1);
  });
  secondaryMods.innerHTML = sm_pick;

  soc_pick =
    s_pick.overclocks[Math.floor(Math.random() * s_pick.overclocks.length)];
  secondaryOCFrame.src = soc_pick.type;
  secondaryOCPic.src = soc_pick.pic;
  secondaryOCName.innerHTML = soc_pick.name;

  //Throwable
  throwableName.innerHTML =
    arsenal.throwables[Math.floor(Math.random() * arsenal.throwables.length)];
}
